public class InfiniteLoops {
    public static void main(String[] args) {
        // Infinite for loop (without any exit condition.)
        System.out.println("Starting infinite for loop...");
        for (;;) { 
            System.out.println("This is an infinite for loop!");
        }

        // Infinite while loop
        System.out.println("Starting infinite while loop...");
        while (true) {
        System.out.println("This is an infinite while loop!");
         }
    }
}



