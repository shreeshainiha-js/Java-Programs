
public class Pattern {

	public static void main(String[] args) {
		int n=5;
		for(int i=0;i<n;i++) //rows 
			{
			for(int j=0;j<n;j++)//colums (both rows and column counts are same)
			{
				System.out.print("*");
			}
			System.out.println();	
			System.out.print("\n");
			}

	}

}

